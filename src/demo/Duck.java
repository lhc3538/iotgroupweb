public class Duck {
    //小鸭子的名称
    private String name;
    //小鸭子的颜色
    private String color;
    //get与set方法
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getColor() {
        return color;
    }
    public void setColor(String color) {
        this.color = color;
    }
}
